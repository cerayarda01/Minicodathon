package GameplayObjects;



public class Character {

    protected int Str;
    protected int Int;
    protected int Wis;
    protected int Luc;








    public int getStr() {
        return Str;
    }

    public void setStr(int str) {
        Str = str;
    }

    public int getInt() {
        return Int;
    }

    public void setInt(int anInt) {
        Int = anInt;
    }

    public int getWis() {
        return Wis;
    }

    public void setWis(int wis) {
        Wis = wis;
    }

    public int getLuc() {
        return Luc;
    }

    public void setLuc(int luc) {
        Luc = luc;
    }
}
